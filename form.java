    private void encryptActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_encryptActionPerformed
        // TODO add your handling code here:
        String a = input.getText();
        String b = inP.getText();
        String c = inQ.getText();
        if (a.trim().isEmpty() || b.trim().isEmpty() || c.trim().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Please fill all required input!");
        } else {
            int Q = Integer.parseInt(inQ.getText());
            int P = Integer.parseInt(inP.getText());
            String msg = input.getText();
            int count1 = 0, count2 = 0;
            for (int i = 2; i < P; i++) {
                if (P % i == 0) {
                    count1++;
                }
            }
            for (int i = 2; i < Q; i++) {
                if (Q % i == 0) {
                    count2++;
                }
            }

            if (count1 > 0) {
                JOptionPane.showMessageDialog(null, "P must be Prime");
            } else if (count2 > 0) {
                System.out.println("Q must be prime");
                JOptionPane.showMessageDialog(null, "Q must be Prime");
            } else {

                output.setText(encode(Q, P, msg)[0]);
                pub.setText(encode(Q, P, msg)[1]);
                priv.setText(encode(Q, P, msg)[2]);
            }

        }
    }//GEN-LAST:event_encryptActionPerformed

