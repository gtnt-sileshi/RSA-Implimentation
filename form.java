    private void decryptActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_decryptActionPerformed
        // TODO add your handling code here:
        String a = output.getText();
        String b = inP.getText();
        String c = inQ.getText();
        if (a.trim().isEmpty() || b.trim().isEmpty() || c.trim().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Please fill all required input!");
        } else {
            int Q = Integer.parseInt(inQ.getText());
            int P = Integer.parseInt(inP.getText());
            String cipher = output.getText();
            int count1 = 0, count2 = 0;
            for (int i = 2; i < P; i++) {
                if (P % i == 0) {
                    count1++;
                }
            }
            for (int i = 2; i < Q; i++) {
                if (Q % i == 0) {
                    count2++;
                }
            }

            if (count1 > 0) {
                System.out.println("P must be prime");
                JOptionPane.showMessageDialog(null, "P must be Prime");
            } else if (count2 > 0) {
                System.out.println("Q must be prime");
                JOptionPane.showMessageDialog(null, "Q must be Prime");
            }
            input.setText(decode(Q, P, cipher)[0]);
            pub.setText(decode(Q, P, cipher)[1]);
            priv.setText(decode(Q, P, cipher)[2]);
        }
    }//GEN-LAST:event_decryptActionPerformed

